#we have seen the fundamentals of python struture like syntax, indentations, comments, block statements
#we have also seen the basic data types in python like integers, floats, strings, booleans, and complex numbers
#now it is time to make those data types useful by performing operations on them
#we shall start with arithmetic operations
#arithmetic operations are the basic mathematical operations like addition, subtraction, multiplication, division, modulus
a = 10
b = 5
#addition
c = a + b
print("Addition:", c)  #this will print 15
#subtraction
d = a - b
print("Subtraction:", d)  #this will print 5
#multiplication
e = a * b
print("Multiplication:", e)  #this will print 50
#division
f = a / b
print("Division:", f)  #this will print 2.0
#note: division always returns a float
#floor division
g = a // b
print("Floor Division:", g)  #this will print 2
#note: floor division returns the largest integer less than or equal to the division result
#modulus
h = a % b
print("Modulus:", h)  #this will print 0
#note: modulus returns the remainder of the division
#exponentiation
i = a ** b
print("Exponentiation:", i)  #this will print 100000
#note: exponentiation raises a to the power of b
