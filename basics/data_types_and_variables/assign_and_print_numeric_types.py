#we have now mastered some syntax and rules of python 
#let's move on to data types and variables
#we first start with numeric types
#in python, there are three main numeric types: int, float, and complex
#int is used to represent whole numbers
#float is used to represent decimal numbers
#and complex is used to represent complex numbers
var1 = 10        #this is an integer
var2 = 3.14      #this is a float
var3 = 2 + 3j    #this is a complex number
print(var1)      #this will print the integer value
print(var2)      #this will print the float value
print(var3)      #this will print the complex number value
