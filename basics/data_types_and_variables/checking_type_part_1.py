# now that we know some data types, we can check the type of a variable
# we can use the built-in type() function to check the type of a variable
a = 10          # this is an integer
b = 3.14        # this is a float
c = 2 + 3j      # this is a complex number
d = 'Hello'     # this is a string
e = True        # this is a boolean value
print(type(a))  # this will print <class 'int'>
print(type(b))  # this will print <class 'float'>
print(type(c))  # this will print <class 'complex'>
print(type(d))  # this will print <class 'str'>
print(type(e))  # this will print <class 'bool'>
