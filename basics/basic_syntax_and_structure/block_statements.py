#we have seen all the basic building blocks of Python programs. 
#Now we will see how to group statements together into blocks.
if True:
    print('this line is part of the block')
    print('this line is also part of the block')
print('this line is outside the block')
#in the example above, the two print statements inside the if statement are part of the block.
#the block is defined by the indentation. The standard practice is to use 4 spaces for indentation.
#the last print statement is outside the block because it is not indented.
#blocks are used to group statements together. They are used in conditionals, loops, functions and classes.
#we will see more about blocks when we learn about these concepts.